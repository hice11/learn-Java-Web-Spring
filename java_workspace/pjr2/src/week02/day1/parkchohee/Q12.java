package week02.day1.parkchohee;

public class Q12 {
	
	public static void main(String[] args) {
		
		//문제12. 전체구구단 출력해 보기
		
		// 반복문
		// 구구단 출력
		int n=2;
		while(n<=9) {
			for (int i = 1; i <= 9; i++) {
				System.out.println(n+" x "+i+" = "+(n*i));
			}
			n++;
			System.out.println("=============");
		}
		
		
		
		///
	}

}
