		//연산을 수행하면 연산의 결과가 발생됨
		//관계연산자 -> true, false
		int myAge=27;
		boolean result = myAge >25;
		System.out.println(result); // true
		
		System.out.println(myAge == 25); // false
		System.out.println(myAge >= 25); // true
		System.out.println(myAge <= 25); // false
		System.out.println(myAge != 25); // true
		System.out.println(myAge < 25); // false
		
		
